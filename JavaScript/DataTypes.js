// Number Data Type: All number like (whole, decimal, positive, negative)
var a = 12;
console.log(a, typeof a);

// Strings: single characters or series of characters all are String

var b = 'A';
console.log(b, typeof b);

// Boolean: True / False

var c = true;
console.log(c, typeof c);

// undefined:

var d;
console.log(d, typeof d); // Undefined

// null

var e = null;
console.log(e, typeof e); // Null

// Infinity : any numbers divided by 0

var f = 123/0;
console.log(f, typeof f);